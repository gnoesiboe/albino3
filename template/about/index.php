<section>
  <h1 class="title-first">Over lijflied</h1>
  <p><strong>Lijflied wil jouw leven mooier maken met muziek.</strong></p>
  <p>Of je muziek nu gebruikt om je talent te ontwikkelen, als uitlaatklep, of om persoonlijke problemen aan te pakken, dat bepaal jij.</p>
  <p>Lijflied is een muziekpraktijk in Goirle, Noord Brabant. Je kunt hier terecht voor lessen, muziektherapie en workshops waar jouw eigenheid centraal staat.</p>
</section>

<section>
  <h2 class="title-second">Visie</h2>
  <ol class="list-number">
    <li>Het beste resultaat krijg je wanneer je op zoek gaat naar de eigenheid van de persoon en dit als uitgangspunt neemt. Je word gestimuleerd datgene te doen wat je hart je ingeeft, en je minder aan te trekken van wat anderen zeggen en doen.</li>
    <li>Open en eerlijk communiceren zorgt voor een goede onderlinge relatie.</li>
    <li>Lijflied gaat uit van een positieve benadering. Complimenteren staat hoog in het vaandel.</li>
    <li>Wat je doet bij Lijflied maakt je trots, sterker, je zelfvertrouwen groeit en je leven krijgt meer kleur.</li>
    <li>Door een goede balans tussen ontspanning en uitdaging blijf je groeien zonder jezelf uit het oog te verliezen.</li>
  </ol>
</section>

<section>
  <h2 class="title-second">Wie is Lijflied?</h2>
  <p>Mijn naam is Christel van der Bruggen en ik wil jouw leven mooier maken met muziek. Na mijn hbo muziekopleiding, met als hoofdvak songwriting, heb ik mijn diploma als muziektherapeut gehaald. Hierdoor heb ik de kans gekregen om muziek te combineren met het echte leven, met alle ups en downs die we tegenkomen.</p>
  <p>Ik woon samen met m’n man Gijs en Dalmatiër Wiebe in Goirle, Noord Brabant. Hier is ook mijn praktijkruimte, of ‘t bijhuis’ zoals ik het zelf vaak noem. </p>
  <p> Christel is aangesloten bij de NVvMT en is kandidaat-geregistreerd bij de FVB. </p>
  <p>Toekomstwensen? Meer mensen kennis laten maken met het krachtige middel dat muziek is om je leven mooier te maken. Zónder dat het zweverig hoeft te zijn. Gewoon recht toe recht aan genieten van en door muziek.</p>
</section>

