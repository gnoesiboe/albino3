<section>
  <h2 class="title-second" id="songwriting-en-zang-voor-mantelzorgers">Songwriting en zang voor mantelzorgers</h2>
  <p>Mantelzorgers staan dag en nacht klaar voor hun naaste. Maar wie zorgt er voor de mantelzorgers? Ben je toe aan tijd voor jezelf en hou je van muziek, dan is deze workshop ideaal. Aan de hand van thema’s gaan we aan de slag om jouw batterij weer op te laden.</p>
  <blockquote>Bij de workshop voor mantelzorgers leer ik weer naar mezelf te luisteren en kan ik even helemaal met mezelf bezig zijn.</blockquote>
  <p>We leren je te ontspannen en te luisteren naar wat jij nodig hebt. We geven gevoelens een plek. Je leert meer over jezelf, krijgt meer zelfvertrouwen en leert om soms ook voor jezelf te kiezen. Alles zodat jij die mooie maar zware taak als mantelzorger vol kan houden.</p>

  <p>Deze 6 weekse workshop kost 190 euro. </p>
  <p>Dinsdag 20:00 - 21:30. Data: 7, 14, 21, 28 januari, 4 en 11 februari</p>
</section>

<section>
  <h2 class="title-second" id="spraak-zang-workshop-voor-mensen-met-afasie">Spraak/zang workshop voor mensen met afasie</h2>
  <p>Zingen maakt het spreken makkelijker! Deze workshop heeft als doel verbaal sterker te worden en te leren om in moeilijke gesprekssituaties toch duidelijk te maken wat je wilt. Dit alles op een laagdrempelige en ontspannen manier ondersteund door melodie en ritme. Daarnaast heeft het zingen van bekende liedjes een grote invloed op ons taalvermogen. Samen, gezellig en nuttig!</p>
  <p>
    Voorbeeld:<br />
    Mark vind het lastig om antwoord te geven op de volgende vraag: Hoe drink je je koffie? met melk, suiker of allebei?’. In de workshop oefenen we het antwoord ‘Koffie met melk en suiker’, door het te zingen. Melodie en ritme zorgen voor een geheugensteuntje om de juiste woorden te vinden en om deze uit te spreken.
  </p>
  <p>Deze 6 weekse workshop kost 160 euro.</p>
  <p>Dinsdag 14:00 - 15:00. Data: 7, 14, 21, 28 januari, 4 en 11 februari</p>
</section>

<section>
  <h2 class="title-second" id="therapeutisch-lied-schrijven-voor-mensen-met-een-langdurige-of-chronische-ziekte">Therapeutisch lied-schrijven voor mensen met een langdurige of chronische ziekte</h2>
  <p>(zoals kanker, spierziekte, dwarslaesie, pijnsyndroom enz.)</p>
  <p>
    Je bent ziek en er komt behoorlijk wat op je bordje; misschien chemo, operatie of een andere vorm van behandeling. Of misschien juist geen behandeling meer. En daarnaast moet je wat met al die nieuwe gevoelens.<br />
    Tegenstrijdige emoties zoals verdriet, hoop, angst, moed en boosheid zijn aan de orde van de dag. In deze workshop kun je hieraan uiting geven door, onder begeleiding, een lied te schrijven. Therapeutisch lied schrijven geeft je meer inzicht in de situatie en je gevoelens en draagt bij aan je psychisch en sociaal welzijn. Daarnaast is het effect dat negatieve gevoelens en gespannenheid minder worden en je beter om kan gaan met je ziekte.
  </p>
  <p>Deze 6 weekse workshop kost 190 euro.</p>
  <p>maandag 20:00 - 21:30 Data: 6, 13, 20, 27 januari, 3 en 10 februari</p>
</section>

