<section>
  <h2 class="title-second" id="muziek-als-therapie-voor-mensen-met-nah">Muziek als therapie voor mensen met NAH</h2>
  <p>(Niet-angeboren Hersenletsel)</p>
  <p>Als je hersenletsel hebt, gaan je dagelijkse dingen vaak wat moeilijker en minder vanzelfsprekend. Ook ben je misschien als persoon wel veranderd en ben je bijvoorbeeld sneller emotioneel of kwaad. In deze workshop krijgt zowel de praktische kant als de emotionele kant aandacht. We gaan aan de slag met het stimuleren van je geheugen om niet meer alles te vergeten, we leren je jouw emoties te uiten, we werken aan ontspanning en we leren je om die ‘nieuwe ik’ te accepteren. Dit alles met muziek voor laagdrempeligheid en ontspanning.</p>
  <p>Deze 6 weekse workshop kost 160 euro.</p>
  <p>Dinsdag 11:00 - 12:00. Data: 7, 14, 21, 28 januari, 4 en 11 februari</p>
</section>

