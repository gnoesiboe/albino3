<strong>Les + een extraatje</strong>

<p>Het unieke van Lijflied zit ‘m in het feit dat er naast je muzikale ontwikkeling ook aandacht is voor jouw persoonlijke ontwikkeling. Toch best eng om voor anderen te spelen/zingen, of lastig om zoveel van jezelf in de muziek te leggen? Ook in deze persoonlijke aspecten van je les word je begleid.</p>
<p>Jouw muziek, jouw liedjes of jouw stem, zijn direct verbonden met jou als persoon. Een winwin situatie dus als je niet alleen verder geholpen wordt met je muzikale ambities, maar ook groeit als persoon. Andere muzieklessen schieten hierin vaak tekort, terwijl het effect groot is; een betere muzikant èn een evenwichtiger persoon!</p>

