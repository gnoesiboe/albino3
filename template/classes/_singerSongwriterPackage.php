  <h2 id="singer-songwriter-pakket" class="title-second">Het Singer/songwriter pakket</h2>
  <p>= songwritingles + zangles + performanceles.</p>
  <p>Je eigen liedjes maken, zingen en uitvoeren. Songwritingles wordt gecombineerd met zangles en er wordt aandacht geschonken aan de manier waarop je speelt/zingt. Belangrijk hierin is dat jij als individu gezien wordt; namelijk iemand die gelooft in wat hij/zij doet, zingt, schrijft en speelt, en dit uitstraalt.</p>
</section>